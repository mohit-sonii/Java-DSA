

public class loops {

    public static void main(String ags[]){
        //for loop
        for(int i=0;i<11;i++)
        {
            System.out.println(i);
        }
        //while loop
        int i=0;
        while(i<11)
        {
            System.out.println(i);
            i++;
        } 
        //do while loop
        int j=0;
        do{
            System.out.println(j);
            j++;
        }while(j<11);

    }
}
